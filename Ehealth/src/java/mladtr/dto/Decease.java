package mladtr.dto;

public class Decease {

	 String deceaseId;
	 String deceaseName;
	 String treatment;
	 String cure;
	 String prevention;
	 String symptom1;	 
	 String symptom2;
	 String symptom3;	
	 
	public String getDeceaseId() {
		return deceaseId;
	}
	public void setDeceaseId(String deceaseId) {
		this.deceaseId = deceaseId;
	}
	public String getDeceaseName() {
		return deceaseName;
	}
	public void setDeceaseName(String deceaseName) {
		this.deceaseName = deceaseName;
	}
	public String getTreatment() {
		return treatment;
	}
	public void setTreatment(String treatment) {
		this.treatment = treatment;
	}
	public String getCure() {
		return cure;
	}
	public void setCure(String cure) {
		this.cure = cure;
	}
	public String getPrevention() {
		return prevention;
	}
	public void setPrevention(String prevention) {
		this.prevention = prevention;
	}
	public String getSymptom1() {
		return symptom1;
	}
	public void setSymptom1(String symptom1) {
		this.symptom1 = symptom1;
	}
	public String getSymptom2() {
		return symptom2;
	}
	public void setSymptom2(String symptom2) {
		this.symptom2 = symptom2;
	}
	public String getSymptom3() {
		return symptom3;
	}
	public void setSymptom3(String symptom3) {
		this.symptom3 = symptom3;
	}	

}
